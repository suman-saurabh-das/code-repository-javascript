/**
 * 01. MODULES
 *
 * • Modules: divide the program into sections of code that by some criteria belongs together.
 * • The benefit of this separation is to help people find what they are looking for.
 * • And by keeping related code together, we also have separation of concerns.
 *
 * • If we have 2 files moduleA.js and moduleB.js then each file becomes a module.
 * • And a variable or function can be exported from moduleB and imported and used at moduleA.
 * • To use modules, we must add a property "type": "module" in package.json file.
 *
 * • In ES2015 there are 2 types of exports: named and default.
 */
